# Création d'une variable int
monInt = 35;
print(type(monInt), monInt, '\n');

# Création d'une variable float
monFloat = 3.14;
print(type(monFloat), monFloat, '\n');

# Création d'une variable complex
monComplex = 26j;
print(type(monComplex), monComplex, '\n');

# Création d'une variable bool
monBool = True;
print(type(monBool), monBool, '\n');

# Création d'une variable str
monString = "Hello";
print(type(monString), monString, '\n');

